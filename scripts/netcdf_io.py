"""
=============================================================================
netcdf_io.py  —  Shared CF-1.10 + ACDD-1.3 writer helpers for the project
=============================================================================

Single source of truth for writing NetCDF outputs in this project
(Scotese paleo-DEMs, Zhou geochem correction, Young 2022 dyntopo
composition test, etc.).

Every NetCDF written via these helpers carries:

  GLOBAL attributes (CF-1.10 + ACDD-1.3 minimum set):
    Conventions          "CF-1.10, ACDD-1.3"
    title                short human-readable description
    summary              ~2-sentence description of what's in the file
    comment              free-form provenance (which script generated it,
                         from what inputs, what conventions, sign convention,
                         etc.) — the verbose explanation
    institution          "EarthByte Group, University of Sydney"
    source               specific software/script + version + commit
    references           upstream literature citations
    history              ISO-8601 timestamp + tool + command (CF convention:
                         each new processing step appends to this string)
    creator_name/_email  ACDD
    date_created         ISO-8601

  COORDINATE-VARIABLE attributes (CF — essential for GMT/QGIS recognition):
    lat:  units="degrees_north", standard_name="latitude",   axis="Y", long_name
    lon:  units="degrees_east",  standard_name="longitude",  axis="X", long_name

  DATA-VARIABLE attributes (per-variable):
    units                e.g. "m", "mm yr-1", "Gt C yr-1"
    long_name            human-readable description
    standard_name        CF standard name if applicable
    _FillValue           explicit for float types (default NaN)

Two entry points:

  open_cf_dataset(path, ...)        — context manager. Use like:
      with open_cf_dataset(out_fp, title=..., summary=..., comment=...) as ds:
          ds.create_lat_lon(lat_arr, lon_arr)
          ds.create_data_var("M_corrected", arr, units="m", long_name="...")

  write_cf_grid(path, fields, ...)  — one-shot convenience. Writes a single
      2D grid (lat × lon) with multiple data variables.

The helper also produces sensible defaults (zlib compression, deflate=4 +
shuffle, lossy bitgroom for floats via least_significant_digit=3 by default)
so files come out compact without needing per-call tuning.

USAGE
    from netcdf_io import write_cf_grid

    write_cf_grid(
        out_path=Path("data/corrected_Scotese/50Ma_corrected_SW.nc"),
        lat=lat, lon=lon,
        fields={
            "M_corrected": (M_final, {"units": "m",
                                       "long_name": "Geochem-corrected paleo-elevation",
                                       "standard_name": "surface_altitude"}),
            "M_orig":      (M, {"units": "m", "long_name": "Original Scotese & Wright 2018 paleo-elevation"}),
            "delta":       (M_final - M, {"units": "m", "long_name": "Correction applied"}),
        },
        title="Geochem-assimilated Scotese & Wright paleo-DEM, 50 Ma",
        summary="Zhou et al. 2024 correction applied to Scotese & Wright 2018 PaleoDEMs.",
        comment="Generated by assimilate_scotese.py from CSV crustal-thickness "
                "geochemistry samples reconstructed via Scotese-Wright 2023 plate model.",
        source="assimilate_scotese.py v1.0 (Paleotopo_data_assimilation, git <hash>)",
        references="Scotese & Wright 2018; Zhou et al. 2024 ESS; Scotese & Wright 2023",
        target_age_ma=50,
    )

This module is intentionally dependency-free beyond netCDF4 + numpy.
=============================================================================
"""
